    private void listarFiadorTodos() {
        try {
            FiadorDAO fiador = new FiadorDAO();
            ArrayList<Fiador> fiadores = fiador.listarFiadorTodos();
            DefaultTableModel limpar = (DefaultTableModel) jTableFiador.getModel();
            limpar.setNumRows(0);
            for (Fiador f : fiadores) {
                DefaultTableModel model = (DefaultTableModel) jTableFiador.getModel();
                Object linha[] = new Object[]{f.getCodigo(), f.getNome(), f.getEndereco(), f.getNumero(), f.getBairro(), f.getCidade(), f.getEstado(), f.getCpf(), f.getTelefone()};
                model.addRow(linha);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao exibir bancos.");
        }
    }
    private void listarBancosTodos() {
        try {
            BancoDAO banco = new BancoDAO();
            ArrayList<Banco> bancos = banco.listarBancoTodos();
            DefaultTableModel limpar = (DefaultTableModel) jTableBanco.getModel();
            limpar.setNumRows(0);
            for (Banco b : bancos) {
                DefaultTableModel model = (DefaultTableModel) jTableBanco.getModel();
                Object linha[] = new Object[]{b.getCodigo(), b.getRazaoSocial(), b.getCnpj(), b.getEndereco(), b.getNumero(), b.getBairro(), b.getCidade(), b.getEstado(), b.getTelefone()};
                model.addRow(linha);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao exibir bancos.");
        }
    }

