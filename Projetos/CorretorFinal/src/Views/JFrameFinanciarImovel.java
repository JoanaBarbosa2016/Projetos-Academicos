    private void buttonVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonVenderActionPerformed
        Banco b = new Banco();
        Fiador f = new Fiador();
        int imovel = Integer.parseInt(jTxtCodImovel.getText());
        int comprador = Integer.parseInt(jTxtCodComprador.getText());
        b.setCodigo((int) jTableBanco.getValueAt(jTableBanco.getSelectedRow(), 0));
        f.setCodigo((int) jTableFiador.getValueAt(jTableFiador.getSelectedRow(), 0));
        try {
                VendaEReservaDAO venda = new VendaEReservaDAO();
                venda.vendaComBanco(imovel, comprador, b.getCodigo(), f.getCodigo());

                JOptionPane.showMessageDialog(null, "Imóvel vendido com sucesso");
                dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Falha!" + e.getMessage());
            }
    }//GEN-LAST:event_buttonVenderActionPerformed

